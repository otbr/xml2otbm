function onCast(cid, creaturePos, level, maglv, var)
centerpos = {x=creaturePos.x, y=creaturePos.y, z=creaturePos.z}
targetpos = getPosition(var)
base = (level*2)+(maglv*3)
 minDmg = base*2.4
 maxDmg = base*2.7
if targetpos.x ~= nil and targetpos.z ~= nil and targetpos.y ~= nil then
	if math.abs(targetpos.x - centerpos.x) < 18 and math.abs(targetpos.y - centerpos.y) < 14 and targetpos.z == centerpos.z then
			doMagic(cid, area, targetpos, damageEffect, areaEffect, animationEffect, animationColor, offensive, physical, minDmg, maxDmg, needDirection, npcRune, type)
	end
end
end



